import { join } from 'node:path';
import { writeGeneratedJsFile, isFileManuallyModified, ensureDir } from '../utils/fs-helpers.js';

/**
 * Write a generated hook file with modification protection.
 * Skips writing if the file was manually modified (unless force is set).
 *
 * @param {string} filePath
 * @param {string} content
 * @param {object} [options]
 * @param {boolean} [options.force]
 * @returns {{ path: string, skipped: boolean }}
 */
function writeGeneratedHook(filePath, content, options = {}) {
  if (!options.force && isFileManuallyModified(filePath)) {
    return { path: filePath, skipped: true };
  }
  writeGeneratedJsFile(filePath, content);
  return { path: filePath, skipped: false };
}

/**
 * Generate Claude Code hooks for quality gate enforcement.
 * Creates Node.js scripts in .claude/hooks/ that run on specific events.
 *
 * @param {object} config - Parsed swarm.yaml config
 * @param {object} projectPaths - Project paths from getProjectPaths()
 * @param {object} [options] - Options
 * @param {boolean} [options.force] - Overwrite even if manually modified
 * @returns {{ generated: string[], skipped: string[] }} Lists of generated and skipped hook file paths
 */
export function generateHooks(config, projectPaths, options = {}) {
  ensureDir(projectPaths.hooksDir);
  const generated = [];
  const skipped = [];

  const hooks = [
    { name: 'post-compact-reinject.cjs', generator: generatePostCompactReinjectHook },
    { name: 'user-prompt-submit.cjs', generator: generateUserPromptSubmitHook },
    { name: 'teamcreate-gate.cjs', generator: generateTeamCreateGateHook },
    { name: 'orchestrator-write-gate.cjs', generator: generateOrchestratorWriteGateHook },
  ];

  for (const { name, generator } of hooks) {
    const filePath = join(projectPaths.hooksDir, name);
    const result = writeGeneratedHook(filePath, generator(config), options);
    if (result.skipped) {
      skipped.push(filePath);
    } else {
      generated.push(filePath);
    }
  }

  return { generated, skipped };
}

/**
 * Return the hooks configuration object for settings.json.
 * This is the SINGLE SOURCE OF TRUTH for all hook definitions.
 *
 * @returns {object} Hooks configuration for settings.json
 */
export function getHooksConfig() {
  return {
    UserPromptSubmit: [
      { hooks: [{ type: 'command', command: 'node .claude/hooks/user-prompt-submit.cjs' }] },
    ],
    SessionStart: [
      { matcher: 'compact', hooks: [{ type: 'command', command: 'node .claude/hooks/post-compact-reinject.cjs' }] },
    ],
    PreToolUse: [
      { matcher: 'TeamCreate', hooks: [{ type: 'command', command: 'node .claude/hooks/teamcreate-gate.cjs' }] },
      { matcher: 'Edit|Write|MultiEdit', hooks: [{ type: 'command', command: 'node .claude/hooks/orchestrator-write-gate.cjs' }] },
    ],
  };
}

function generateUserPromptSubmitHook(config) {
  return `#!/usr/bin/env node
// BMAD Swarm - UserPromptSubmit Hook (minimal reminder)
// Generated by bmad-swarm - do not edit manually
//
// On a fresh session, emits a short reminder pointing the orchestrator
// at the /identity-orchestrator slash command. After the first user
// turn in a session, emits nothing (the marker suppresses re-fires).

const fs = require('fs');
const path = require('path');

let input = '';
process.stdin.setEncoding('utf8');
process.stdin.on('data', (chunk) => { input += chunk; });
process.stdin.on('end', () => {
  let event;
  try { event = JSON.parse(input); } catch { process.exit(0); }
  const cwd = event.cwd || process.cwd();
  const markerPath = path.join(cwd, '.claude', 'hooks', '.session-active');
  if (fs.existsSync(markerPath)) { process.exit(0); }
  try { fs.writeFileSync(markerPath, Date.now().toString()); } catch {}
  process.stdout.write(JSON.stringify({
    hookSpecificOutput: {
      hookEventName: 'UserPromptSubmit',
      additionalContext: 'Type /identity-orchestrator to load your role, or /bug /feature /research /audit /brainstorm /migrate /review /plan for common workflows.',
    },
  }));
  process.exit(0);
});
`;
}

function generatePostCompactReinjectHook(config) {
  return `#!/usr/bin/env node
// BMAD Swarm - Post-compaction identity reminder
// Generated by bmad-swarm - do not edit manually
//
// Fires on SessionStart with matcher 'compact'. Clears the session
// marker (so the UserPromptSubmit reminder re-fires on the next turn)
// and emits a one-shot pointer to /identity-orchestrator so the
// compacted session can restore its full role.

const fs = require('fs');
const path = require('path');
const markerPath = path.join(process.cwd(), '.claude', 'hooks', '.session-active');
try { fs.unlinkSync(markerPath); } catch {}
process.stdout.write(JSON.stringify({
  hookSpecificOutput: {
    hookEventName: 'SessionStart',
    additionalContext: 'Context was compacted. You are the orchestrator of bmad-swarm. To restore full identity: invoke /identity-orchestrator. Slash commands: /bug /feature /research /audit /brainstorm /migrate /review /plan. Never use Task for project delegation — use TeamCreate. Never edit code or artifacts yourself.',
  },
}));
process.exit(0);
`;
}

function generateTeamCreateGateHook(config) {
  return `#!/usr/bin/env node
// BMAD Swarm - PreToolUse gate on TeamCreate
// Generated by bmad-swarm - do not edit manually
//
// Parses the JSONL transcript and requires a fenced bmad-assembly block with
// the required keys to appear in the last N=5 assistant message.ids. Accepts
// both 'text' and 'thinking' content blocks. Widening the search from the
// single last message.id to the last 5 message.ids covers streaming splits,
// retries after transient failures, and the common 'plan in message → tool_use
// in next message' pattern. Fails open when the transcript is missing or
// unreadable. This is a shape check, not an authorization boundary.
//
// GATE-1 (2.0.2): raw-transcript-scan fallback. When the structured content-
// block walk does not surface the fence — observed when an assistant message
// starts with only a fenced bmad-assembly block and the transcript JSON
// encodes the content in a shape the typed walk misses — a secondary pass
// scans the raw transcript text for the fence. Both passes enforce the same
// 5-key requirement; only denies if NEITHER finds a valid block.

const fs = require('fs');
let input = '';
process.stdin.setEncoding('utf8');
process.stdin.on('data', (c) => { input += c; });
process.stdin.on('end', () => {
  let event;
  try { event = JSON.parse(input); } catch { process.exit(0); }
  const transcriptPath = event.transcript_path;
  if (!transcriptPath || !fs.existsSync(transcriptPath)) { process.exit(0); }
  let raw;
  try { raw = fs.readFileSync(transcriptPath, 'utf8'); } catch { process.exit(0); }

  const entries = raw.split(/\\r?\\n/).filter(Boolean)
    .map(l => { try { return JSON.parse(l); } catch { return null; } })
    .filter(e => e && e.type === 'assistant' && e.message);
  const REQUIRED_KEYS = ['entry_point', 'complexity', 'autonomy', 'team', 'rationale'];
  const FENCE_RE = /\`\`\`bmad-assembly\\s*\\n([\\s\\S]*?)\\n\`\`\`/;
  function deny(missing) {
    process.stdout.write(JSON.stringify({
      hookSpecificOutput: {
        hookEventName: 'PreToolUse',
        permissionDecision: 'deny',
        permissionDecisionReason: 'Blocked: produce a bmad-assembly block (with keys: ' + REQUIRED_KEYS.join(', ') + ') in your message BEFORE calling TeamCreate. Missing: ' + missing.join(', '),
      },
    }));
  }
  function missingKeys(block) {
    if (!block) return REQUIRED_KEYS.slice();
    return REQUIRED_KEYS.filter(k => !new RegExp('^\\\\s*' + k + ':', 'm').test(block));
  }
  if (entries.length === 0) { deny(REQUIRED_KEYS); process.exit(0); }

  // Pass 1: structured content-block walk over last N=5 distinct assistant
  // message.ids, accepting both 'text' and 'thinking' blocks.
  const N = 5;
  const recentIds = new Set();
  for (let i = entries.length - 1; i >= 0 && recentIds.size < N; i--) {
    recentIds.add(entries[i].message.id);
  }
  const texts = [];
  for (const e of entries) {
    if (!recentIds.has(e.message.id)) continue;
    const content = Array.isArray(e.message.content) ? e.message.content : [];
    for (const b of content) {
      if (!b) continue;
      if (b.type === 'text' && b.text) texts.push(b.text);
      else if (b.type === 'thinking' && b.thinking) texts.push(b.thinking);
    }
  }
  const structuredText = texts.join('\\n');
  const structuredMatch = structuredText.match(FENCE_RE);
  const structuredMissing = missingKeys(structuredMatch ? structuredMatch[1] : null);
  if (structuredMissing.length === 0) { process.exit(0); }

  // Pass 2: raw-transcript-scan fallback. GATE-1 observed that some message
  // shapes don't surface the fence through the typed-block walk; scanning the
  // raw JSON directly catches those. Scope the raw scan to only the raw JSONL
  // lines whose parsed assistant entries belong to the same recent N message
  // ids the structured walk used — preserves the "plan BEFORE create"
  // recency invariant while recovering from walk-misses. The raw JSONL
  // encodes real newlines as the two-char sequence \\n so we un-escape those
  // before matching so a fence that exists only inside a JSON-stringified
  // value still triggers a pass. Also cap the scanned region at ~50KB as a
  // belt-and-braces guard against pathological transcripts.
  const RAW_TAIL_BYTES = 50 * 1024;
  const rawLines = raw.split(/\\r?\\n/);
  const recentRawLines = [];
  for (const line of rawLines) {
    if (!line) continue;
    let parsed;
    try { parsed = JSON.parse(line); } catch { continue; }
    if (parsed && parsed.type === 'assistant' && parsed.message && recentIds.has(parsed.message.id)) {
      recentRawLines.push(line);
    }
  }
  let rawScope = recentRawLines.join('\\n');
  if (rawScope.length > RAW_TAIL_BYTES) {
    rawScope = rawScope.slice(rawScope.length - RAW_TAIL_BYTES);
  }
  const rawUnescaped = rawScope.replace(/\\\\n/g, '\\n');
  const rawMatch = rawUnescaped.match(FENCE_RE);
  if (rawMatch) {
    const rawMissing = missingKeys(rawMatch[1]);
    if (rawMissing.length === 0) { process.exit(0); }
  }

  deny(structuredMissing);
  process.exit(0);
});
`;
}
function generateOrchestratorWriteGateHook(config) {
  return `#!/usr/bin/env node
// BMAD Swarm - PreToolUse gate blocking Edit/Write/MultiEdit for the orchestrator role
// Generated by bmad-swarm - do not edit manually
//
// Two-layer identity check (per ADR-003, HARN-1 fix):
//   (1) Payload signal: Claude Code populates event.agent_id and event.agent_type
//       only when the hook fires inside a subagent (teammate) call. Main-thread
//       orchestrator calls have neither. This is the authoritative discriminator.
//   (2) Env signal: AGENT_ROLE=orchestrator (from settings.json env) is kept as
//       defense-in-depth and as the in-session override path (settings.local.json
//       AGENT_ROLE: '' disables the gate entirely if needed).
// Both checks must say "this is main-thread orchestrator" before the gate fires.
// The gate restricts orchestrator writes to artifacts/** plus project.yaml,
// swarm.yaml, .gitignore, the session-active marker, and settings.local.json.

let input = '';
process.stdin.setEncoding('utf8');
process.stdin.on('data', (c) => { input += c; });
process.stdin.on('end', () => {
  let event;
  try { event = JSON.parse(input); } catch { process.exit(0); }
  // (1) Teammate subagent calls carry agent_id or agent_type. Main-thread
  // orchestrator calls carry neither. Pass through when either is present.
  if (event.agent_id || event.agent_type) { process.exit(0); }
  // (2) Env-var secondary check preserves the local-override recovery path.
  if (process.env.AGENT_ROLE !== 'orchestrator') { process.exit(0); }
  const toolInput = event.tool_input || {};
  const filePath = toolInput.file_path || toolInput.path || '';
  const allowed = [
    /artifacts[\\\\/]/,
    /project\\.yaml$/,
    /swarm\\.yaml$/,
    /\\.gitignore$/,
    /\\.claude[\\\\/]hooks[\\\\/]\\.session-active$/,
    /\\.claude[\\\\/]settings\\.local\\.json$/,
  ];
  const isAllowed = allowed.some(r => r.test(filePath));
  if (isAllowed) { process.exit(0); }
  process.stdout.write(JSON.stringify({
    hookSpecificOutput: {
      hookEventName: 'PreToolUse',
      permissionDecision: 'deny',
      permissionDecisionReason: 'Blocked: orchestrator role cannot edit ' + filePath + '. Delegate code/artifact changes to a teammate via TeamCreate. Allowed orchestrator writes: artifacts/**, project.yaml, swarm.yaml, .gitignore, .claude/settings.local.json.',
    },
  }));
  process.exit(0);
});
`;
}
