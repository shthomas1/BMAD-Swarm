function generateIdentityReinjectHook(config) {
  const projectName = config.project?.name || 'BMAD Swarm';

  return `#!/usr/bin/env node
// BMAD Swarm - SessionStart Identity Reinject Hook
// Generated by bmad-swarm - do not edit manually
//
// Fires on context compaction and session start.
// Clears the session marker so the UserPromptSubmit hook re-triggers
// a full orchestrator.md read on the next user message.

const fs = require('fs');
const path = require('path');

const markerPath = path.join(process.cwd(), '.claude', 'hooks', '.session-active');
try { fs.unlinkSync(markerPath); } catch {}

const output = JSON.stringify({
  hookSpecificOutput: {
    hookEventName: 'SessionStart',
    additionalContext: 'IDENTITY REMINDER: You are the orchestrator of ' + ${JSON.stringify(projectName)} + '. Context was compacted or session restarted. Your full instructions will be re-injected on the next request.',
  },
});
process.stdout.write(output);
process.exit(0);
`;
}

function generateUserPromptSubmitHook(config) {
  const projectName = config.project?.name || 'BMAD Swarm';

  return `#!/usr/bin/env node
// BMAD Swarm - UserPromptSubmit Hook
// Generated by bmad-swarm - do not edit manually
//
// Fires on every user message.
// First message of a session: tells the orchestrator to read orchestrator.md.
// Follow-up messages: injects a short delegation reminder.
// After context compaction: identity-reinject hook clears the marker,
// so the next message is treated as a first message again.

const fs = require('fs');
const path = require('path');

let input = '';
process.stdin.setEncoding('utf8');
process.stdin.on('data', (chunk) => { input += chunk; });
process.stdin.on('end', () => {
  let event;
  try { event = JSON.parse(input); } catch { process.exit(0); }

  const cwd = event.cwd || process.cwd();
  const markerPath = path.join(cwd, '.claude', 'hooks', '.session-active');
  const isFirstMessage = !fs.existsSync(markerPath);

  if (isFirstMessage) {
    try { fs.writeFileSync(markerPath, Date.now().toString()); } catch {}
    const output = JSON.stringify({
      hookSpecificOutput: {
        hookEventName: 'UserPromptSubmit',
        additionalContext: 'ORCHESTRATOR: Before responding, read .claude/agents/orchestrator.md in full. Then assess this request, determine the correct entry point, and follow the routing rules. Do not implement anything directly — delegate all work to specialist agents via TeamCreate.',
      },
    });
    process.stdout.write(output);
  } else {
    const output = JSON.stringify({
      hookSpecificOutput: {
        hookEventName: 'UserPromptSubmit',
        additionalContext: 'You are the orchestrator of ${projectName}. Do not implement anything directly. All coding, research, and analysis must be delegated to specialist agents via TeamCreate.',
      },
    });
    process.stdout.write(output);
  }

  process.exit(0);
});
`;
}

